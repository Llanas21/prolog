    subgenero_genero(Subgenero, Genero).

% 6. Listar todos los géneros y sus respectivos subgéneros
generos_y_subgeneros(ListaGenerosYSubgeneros) :-
    findall(Genero-Subgeneros, (genero(Genero), findall(Subgenero, subgenero_genero(Subgenero, Genero), Subgeneros)), ListaGenerosYSubgeneros).

