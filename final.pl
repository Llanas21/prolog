    subgenero_genero(Subgenero, Genero).

% 2. Listar todos los subgéneros de un género
subgeneros_de_genero(Genero, ListaSubgeneros) :-
    findall(Subgenero, subgenero_genero(Subgenero, Genero), ListaSubgeneros).

% 3. Verificar si un género es válido
es_genero(Genero) :-
    genero(Genero).

